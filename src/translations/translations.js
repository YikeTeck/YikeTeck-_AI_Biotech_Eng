// Funzione per ottenere una traduzione
export function getTranslation(language, key) {
  // Dividi la chiave in parti (es. "navbar.products" => ["navbar", "products"])
  const keys = key.split('.');
  
  // Controlla se la lingua esiste nelle traduzioni
  if (!translations[language]) {
    console.warn(`Language '${language}' not available, fallback to English`);
    language = 'en';
  }
  
  // Inizia dalla radice delle traduzioni per la lingua selezionata
  let result = translations[language];
  
  // Attraversa l'oggetto di traduzione seguendo le parti della chiave
  for (const k of keys) {
    if (!result || !result[k]) {
      console.warn(`Translation key '${key}' not found for language '${language}'`);
      return key; // Ritorna la chiave se la traduzione non è trovata
    }
    result = result[k];
  }
  
  return result;
}

export default translations;